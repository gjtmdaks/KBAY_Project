COMMENT ON COLUMN MEMBER.USER_PWD IS '회원 암호화 비밀번호';
COMMENT ON COLUMN MEMBER.USER_NAME IS '회원 이름';
COMMENT ON COLUMN MEMBER.USER_ADDRESS IS '회원 주소';
COMMENT ON COLUMN MEMBER.USER_PHONE IS '회원 전화번호';
COMMENT ON COLUMN MEMBER.USER_EMAIL IS '회원 이메일';
COMMENT ON COLUMN MEMBER.USER_ENROLL_DATE IS '회원 가입일';
COMMENT ON COLUMN MEMBER.USER_DELETE_YN IS '회원 탈퇴 여부';
COMMENT ON COLUMN MEMBER.USER_LOGIN_IP IS '최근 로그인 IP';
COMMENT ON COLUMN MEMBER.PROFILE_IMG IS '프로필 이미지 경로';
COMMENT ON COLUMN MEMBER.INTRO IS '회원 자기소개';
COMMENT ON COLUMN MEMBER.RATING_SCORE IS '회원 평점';
