COMMENT ON COLUMN ITEM.ITEM_CONTENT IS '상품 설명';
COMMENT ON COLUMN ITEM.CREATE_COUNTRY IS '제조 국가';
COMMENT ON COLUMN ITEM.START_PRICE IS '경매 시작 가격';
COMMENT ON COLUMN ITEM.CURRENT_PRICE IS '현재 입찰 가격';
COMMENT ON COLUMN ITEM.DIRECT_BUY IS '즉시 구매 가능 여부';
COMMENT ON COLUMN ITEM.BUY_NOW_PRICE IS '즉시 구매 가격';
COMMENT ON COLUMN ITEM.START_TIME IS '경매 시작 시간';
COMMENT ON COLUMN ITEM.END_TIME IS '경매 종료 시간';
COMMENT ON COLUMN ITEM.STATUS IS '상품 상태';
